"use client";
import Link from "next/link";
import { usePathname } from "next/navigation";

const tabs = [
  { href: "/home",     label: "ホーム",   icon: HomeIcon },
  { href: "/calendar", label: "カレンダー", icon: CalIcon },
  { href: "/oshi",     label: "推し",     icon: StarIcon },
  { href: "/settings", label: "設定",     icon: SettingsIcon },
];

export default function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      style={{
        position: "fixed",
        bottom: 0,
        left: 0,
        right: 0,
        height: 64,
        background: "var(--color-surface)",
        borderTop: "1px solid var(--color-border)",
        display: "flex",
        alignItems: "center",
        paddingBottom: "env(safe-area-inset-bottom, 0px)",
        zIndex: 50,
      }}
    >
      {tabs.map(({ href, label, icon: Icon }) => {
        const active = pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            style={{
              flex: 1,
              display: "flex",
              flexDirection: "column",
              alignItems: "center",
              justifyContent: "center",
              gap: 3,
              textDecoration: "none",
              color: active ? "var(--color-primary)" : "var(--color-text-3)",
              transition: "color 0.15s",
              paddingTop: 8,
              paddingBottom: 8,
            }}
          >
            <Icon size={22} />
            <span style={{ fontSize: 10, fontWeight: active ? 500 : 400 }}>{label}</span>
          </Link>
        );
      })}
    </nav>
  );
}

